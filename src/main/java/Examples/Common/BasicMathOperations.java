package Examples.Common;

public class BasicMathOperations {

	public static void main(String[] args) {
		//TODO: Add if you find anything interest
		// will convert the number into positive
		System.out.println(Math.abs(-7));
		
		// will o/p nearest next round value
		System.out.println(Math.ceil(2.1));
		
		// will o/p nearest previous round value
		System.out.println(Math.floor(2.8));
				
		// will get max value 
		System.out.println(Math.max(23, 12));
		
		// will get min value
		System.out.println(Math.min(23, 12));
		
		//simply power the value = (value)^n
		System.out.println(Math.pow(4, 3));
		
		//square root of the value
		System.out.println(Math.sqrt(9));
	}
}
